`ADD_M: begin
aux_carry = A[3:0] + mem[{H,L}][3:0];
res = A + mem[{H,L}];
A <= res[7:0];
carry <= res[8];
aux <= aux_carry[4];
zero <= (res[7:0] == 8'b0);
sign <= res[7];
parity <= ~^res[7:0];
cpu_state <= fetch_inst;
end

`ADI: begin
imm_data = mem[PC];
res = A + imm_data;
aux_carry = A[3:0] + imm_data[3:0];
A <= res[7:0];
zero <= (res[7:0] == 8'b0);
sign <= res[7];
carry <= res[8];
aux <= aux_carry[4];
parity <= ~^res[7:0];
PC <= PC + 1;
cpu_state <= fetch_inst;
end

`ANA_A: begin
res[7:0] = A & A;
carry <= 1'b0;
aux <= 1'b0;
