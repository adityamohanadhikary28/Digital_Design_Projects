{H,L} <= temp[15:0];
carry <= temp[16];
cpu_state <= fetch_inst;
end

`DCR_A: begin
res = A - 1;
aux_carry = A[3:0] - 1;
A <= res[7:0]; 
zero <= (res[7:0] == 8'b0);
sign <= res[7];
carry <= res[8];
aux <= aux_carry[4];
