cpu_state <= fetch_inst;
end

`DAA: begin
if(A[3:0] > 4'h6 && A[7:4] < 4'h6) A <= A + 8'h06;
else if(A[3:0] < 4'h6 && A[7:4] > 4'h6) A <= A + 8'h60;
else if(A[3:0] > 4'h6 && A[7:4] > 4'h6) A <= A + 8'h66;
cpu_state <= fetch_inst;
end

`DAD_B: begin
temp = {H,L} + {B,C};
{H,L} <= temp[15:0];
carry <= temp[16];
cpu_state <= fetch_inst;
end

`DAD_D: begin
