`ANI: begin
imm_data = mem[PC];
res[7:0] = A & imm_data;
A <= res[7:0];
zero <= (res[7:0] == 8'b0);
sign <= res[7];
carry <= 1'b0;
aux <= 1'b0;
parity <= ~^res[7:0]; 
