 module cpu_8085(input clk, rst, load, [7:0] din, output [7:0] dout);

reg [7:0] A, B, C, D, E, H, L; //GPR
assign dout = A;
reg [7:0] W,Z; //Temp reg
reg [7:0] IR; //Instruction Register
reg [15:0] PC = 16'h2000, SP = 16'h2000; //SPR
reg [7:0] mem [0:65535]; //64KB memory
reg [7:0] port [0:255]; //8085 ports
reg [8:0] res = 9'b0; 
reg [16:0] temp = 17'b0;
reg [7:0] imm_data = 8'b0;
reg [4:0] aux_carry = 5'b0; //For auxiliary carry flag
reg sign = 1'b0, carry = 1'b0, zero = 1'b0, parity = 1'b0, aux = 1'b0; //8-bit status flags

parameter load_inst = 3'b000, fetch_inst = 3'b010, exec_inst = 3'b011, halt = 3'b100;

reg [2:0] cpu_state = load_inst;
reg [4:0] count = 0;

always @(posedge clk) begin
case(cpu_state)

load_inst: begin
if(load) begin
mem[PC] <= din;
PC <= PC + 1;
cpu_state <= load_inst;
end
else if(rst) begin
PC <= 16'h2000;
cpu_state <= fetch_inst;
end
end

fetch_inst: begin
IR <= mem[PC];
PC <= PC + 1;
cpu_state <= exec_inst;
end

exec_inst: begin
case(IR)

`ACI: begin
imm_data = mem[PC]; 
res = A + imm_data + carry;
aux_carry = A[3:0] + imm_data[3:0] + carry; 
A <= res[7:0];
zero <= (res[7:0] == 8'b0);
sign <= res[7];
carry <= res[8];
aux <= aux_carry[4];
parity <= ~^res[7:0];
PC <= PC + 1;
cpu_state <= fetch_inst;
end

`ADC_A: begin
aux_carry = A[3:0] + A[3:0];
res = A + A + carry;
A <= res[7:0];
carry <= res[8];
aux <= aux_carry[4];
zero <= (res[7:0] == 8'b0);
sign <= res[7];
parity <= ~^res[7:0];
cpu_state <= fetch_inst;
end

`ADC_B: begin
aux_carry = A[3:0] + B[3:0];
res = A + B + carry;
A <= res[7:0];
carry <= res[8];
aux <= aux_carry[4];
zero <= (res[7:0] == 8'b0);
sign <= res[7];
parity <= ~^res[7:0];
cpu_state <= fetch_inst;
end

`ADC_C: begin
aux_carry = A[3:0] + C[3:0];
res = A + C + carry;
A <= res[7:0];
carry <= res[8];
aux <= aux_carry[4];
zero <= (res[7:0] == 8'b0);
sign <= res[7];
parity <= ~^res[7:0];
cpu_state <= fetch_inst;
end

`ADC_D: begin
aux_carry = A[3:0] + D[3:0];
res = A + D + carry;
A <= res[7:0];
carry <= res[8];
aux <= aux_carry[4];
zero <= (res[7:0] == 8'b0);
sign <= res[7];
parity <= ~^res[7:0];
cpu_state <= fetch_inst;
end

`ADC_E: begin
aux_carry = A[3:0] + E[3:0];
res = A + E + carry;
A <= res[7:0];
carry <= res[8];
aux <= aux_carry[4];
zero <= (res[7:0] == 8'b0);
sign <= res[7];
parity <= ~^res[7:0];
cpu_state <= fetch_inst;
end

`ADC_H: begin
aux_carry = A[3:0] + H[3:0];
res = A + H + carry;
A <= res[7:0];
carry <= res[8];
aux <= aux_carry[4];
zero <= (res[7:0] == 8'b0);
sign <= res[7];
parity <= ~^res[7:0];
cpu_state <= fetch_inst;
end

`ADC_L: begin
aux_carry = A[3:0] + L[3:0];
res = A + L + carry;
A <= res[7:0];
carry <= res[8];
aux <= aux_carry[4];
zero <= (res[7:0] == 8'b0);
sign <= res[7];
parity <= ~^res[7:0];
cpu_state <= fetch_inst;
end

`ADC_M: begin
aux_carry = A[3:0] + mem[{H,L}][3:0];
res = A + mem[{H,L}] + carry;
A <= res[7:0];
carry <= res[8];
aux <= aux_carry[4]; 
zero <= (res[7:0] == 8'b0);
sign <= res[7];
parity <= ~^res[7:0];
cpu_state <= fetch_inst;
end

`ADD_A: begin
aux_carry = A[3:0] + A[3:0];
res = A + A;
