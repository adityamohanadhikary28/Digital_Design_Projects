end

`CMP_M: begin
res = {1'b0,A} - {1'b0,mem[{H,L}]};
aux_carry = {1'b0, A[3:0]} - {1'b0, mem[{H,L}][3:0]};
zero <= (res[7:0] == 8'b0);
carry <= res[8];
aux <= aux_carry[4];
sign <= res[7];
parity <= ~^res[7:0];
cpu_state <= fetch_inst;
end

`CPI: begin
imm_data = mem[PC];
res = A - imm_data;
aux_carry = A[3:0] - imm_data[3:0];
zero <= (res[7:0] == 8'b0);
sign <= res[7];
carry <= res[8];
aux <= aux_carry[4];
parity <= ~^res[7:0];
